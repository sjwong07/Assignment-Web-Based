// Shopping Cart
// ============================================================================

// Get shopping cart
function get_cart() {
    return $_SESSION['cart'] ?? [];
}

// Set shopping cart
function set_cart($cart = []) {
    $_SESSION['cart'] = $cart;
}

// Update shopping cart
function update_cart($id, $unit) {
    $cart = get_cart();

    if($unit >= 1 && $unit <= 10 && is_exists($id, 'product', 'id')){
        $cart[$id] = $unit;
        ksort($cart);
    }
    else{
        unset($cart[$id]);
    }

    set_cart($cart);
}

